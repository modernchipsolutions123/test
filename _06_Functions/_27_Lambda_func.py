#Lambda Functions: It is a single-line statement.It is also known as anonymous function.
#this function start with lambda keyword
#x =  lambda no.of arguments : expression
a = lambda x,y : x + y
print(a(10,20))

a = lambda x,y : x **y
print(a(2,4))

b = lambda x , y , z : x+y+z
print(b(2,4,6))

b = lambda x , y , z : x-y-z
print(b(100,4,6))

