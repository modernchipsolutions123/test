#5.Sum all the items in a dictionary
dic2 = {1:10, 2:20, 3:30}
print("All values in dictionary is int",sum(dic2.values()))
print("All keys in dictionary is int",sum(dic2.keys()))

dic2 = {"a":10, "b":20, "c":30}
print("It will print all the keys and values", dic2.items())
print("It will print only keys", dic2.keys())
print("It will print only values", dic2.values())
print("sum of the values int:",sum(dic2.values()))



